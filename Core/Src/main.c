/*
 * V1.0
 * Niall O'Keeffe
 * 18/10/2024
 *
 * FreeRTOS IoT project
 * Connects to Ubidots MQTT broker
 * Retrieves time and date from the network and prints to terminal every second
 * Publishes a temperature when the user button is pressed
 * Subscribes to an MQTT topic that controls LED2
 *
 * An initialisation task is created on startup
 * Initialisation task performs the following functions: -
 * 		Connect to WiFi access point
 * 		Retrieves data and time from network
 * 		Connects to MQTT broker
 * 		Creates semaphores used for task synchronisation
 * 		Creates RTC and Temperature tasks
 * 		Subscribed to an MQTT LED control topic
 *
 * Publish task publishes a temperature to an MQTT topic every 5 seconds on taking a semaphore
 * from the Timer 6 interrupt handler callback
 *
 * RTC task prints time and date every second on taking semaphore from RTC event interrupt
 *  handler callback
 *
 * Project uses 3 timers: -
 * 		Timer 1 is used as RTOS timebase and should not be used by application
 * 		Timer 7 is used for real time RTOS debug statistics and should not be used by application
 *
 * The project application uses the following interrupts
 * 		User button EXTI interrupt
 * 		RTC 1-second event interrupt
 *
 * User credentials must be configured before building the project
 * 		Edit WiFi access point SSID and password in WiFi_Credentials.h
 * 		Edit username, password and clientID in CloudBrokerCredentials.h
 */

